#include <stdio.h>


// A C program is made up of different tokens combined together. These tokens include:

// Keywords
// Identifiers
// Constants
// String Literal
// Symbols

int main()
{
    // Here int is a Keywords AND a Identifiers
    int _a; // ==> AND _a Identifiers

         //    String Literal
    printf("\nENTER FAVOURITE NUMBER : ");
// scanf is functions
    scanf("%d", &_a); // ==> here & is speacial symbol
// %d is format specifier

    // Here return is a Keywords
    return 0; // ==> 0 is a constant
}


