// Flow Chart of Algorithm
// **************************

// FLOW CHART SYMBOLS
// ******************
 
// 1) OVAL  	 ==> Indicates Start and End
// 2) Arrow 	 ==> Indicates Direction of Flow
// 3) Parallelogram ==> Indicates Input/Output Operation
// 4) Rectangle     ==> Indicates Process
// 5) Diamond       ==> Indicates Decision

// Example
// *****************************
// 1) Algorithm Representation of Adding two numbers
// *****************************

// Start    	Enter N1 & N2         Add = N1 + N2	    DISPLAY Add		  End
// OVAL    ------> Parallelogram ------> Rectangle     ------> Parallelogram ------> OVAL

// *****************************
// 2) Algorithm Representation of Age Voting Criteria Eligible or Not Eligible.
// *****************************
// 						     If YES Eligible,
// 						     Parallelogram

// Start, 	     Enter the Age, 	   Check the Age >= 18,                 End
// OVAL ------> Parallelogram ------> Diamond				OVAL
// 						      Else Not Eligible,
// 					              Parallelogram
// *****************************
// 3) Algorithm Representation of Greater Number from Two Numbers.
// *****************************
// 						     If YES N1 Greater,
// 						     Parallelogram

// Start, 	     Enter N1 & N2, 	   Check N1>N2,                         End
// OVAL ------> Parallelogram ------> Diamond				OVAL
// 						      Else Not N2 Greater,
// 					              Parallelogram

// *****************************
// 4) Algorithm Representation of Entered Number is Positive(ve+) or Negative(ve-).
// *****************************
// 						     If YES N ve+,
// 						     Parallelogram

